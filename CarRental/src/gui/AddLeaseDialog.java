    private class AddLeaseWorker extends SwingWorker<Void, Void> {
        private Lease c;

        public AddLeaseWorker(Lease c) {
            this.c = c;
        }
                
        @Override
        protected Void doInBackground() throws Exception {
            ((Index)getParent()).getLeasesManagerPanel().getTableModel().addLease(c);
            return null;
        }
        
        @Override
        protected void done(){
            dispose();
        }
    }
    
