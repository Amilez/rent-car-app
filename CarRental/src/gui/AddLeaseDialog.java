    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        CarManagerImpl carManager = new CarManagerImpl();
        CustomerManagerImpl customerManager = new CustomerManagerImpl();
        
        BasicDataSource ds = new BasicDataSource();
        ds.setUrl("jdbc:derby://localhost:1527/pv168");
        
        carManager.setDataSource(ds);
        customerManager.setDataSource(ds);
        
        Lease lease = new Lease();
        Car car = new Car();
        Customer customer = new Customer();
        
        car.setId(((ComboBoxObject)carCombo.getSelectedItem()).getId());
        customer.setId(((ComboBoxObject)customerCombo.getSelectedItem()).getId());
        
        lease.setCar(car);
        lease.setCustomer(customer);
        lease.setFromDate(jDateSelector1.getSelectedDate());
        lease.setToDate(jDateSelector2.getSelectedDate());
        
        new AddLeaseWorker(lease).execute();
    }//GEN-LAST:event_jButton1ActionPerformed

